		        0xE4, 0x81, 0x13, 0xE0
	        };

	        var encoder = new Rs256EncoderLittleEndian(64, 60, new GaloisField256(0x11D));

			// Verify that encoder doesn't keep a state
			var checksum = new byte[4];
	        encoder.CalculateParity(data, checksum);

			encoder.CalculateParity(data, checksum);

			Assert.That(checksum, Is.EqualTo(new byte[] {0x79, 0x9b, 0x9a, 0xee}));
	        
	        var toBeCorrected = (byte[])data.Clone();
	        toBeCorrected[0] = 255;

	        var corrected = CorrectWithChecksum(toBeCorrected, checksum);

	        Assert.That(corrected, Is.EqualTo(data));
        }

		[Test]
		public void Test2()
        {
	        var data = new byte[]
	        {
		        0x0E, 0x0F, 0x10, 0x11, 0x12, 0x13, 0x9E, 0x26, 0x55, 0x71
	        };

	        var checksum = CreateChecksum(data);

